//Write a function called "printEvery3" that prints out integers 1, 4, 7, 10, ..., 88.
//for loop
function printEvery3(){
    for(let i=1; i<= 88 ; i+=3 ){
        console.log(i);
    }
}
printEvery3();

//while loop 
function printEvery4(){
    let j = 1;
    while(j<=89){
        console.log(j);
        j +=4;
    }
}
printEvery4();